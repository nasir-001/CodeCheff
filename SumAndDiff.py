def main():

	user_input_one = int(input())
	user_input_two = int(input())

	if user_input_one > user_input_two:
		diff = user_input_one - user_input_two
		print(diff)
	else:
		sums = user_input_one + user_input_two
		print(sums)

if __name__ == "__main__":
	main()